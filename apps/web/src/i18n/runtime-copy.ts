function normalizeText(value: string) {
  return value.replace(/\s+/g, ' ').trim();
}

function collectPairs(source: unknown, target: unknown, dictionary: TextDictionary) {
  if (typeof source === 'string' && typeof target === 'string') {
    const key = normalizeText(source);
    if (key && key !== target) {
      dictionary[key] = target;
    }
    return;
  }

  if (Array.isArray(source) && Array.isArray(target)) {
    source.forEach((item, index) => collectPairs(item, target[index], dictionary));
    return;
  }

  if (
    source &&
    target &&
    typeof source === 'object' &&
    typeof target === 'object'
  ) {
    Object.entries(source as UnknownRecord).forEach(([key, value]) => {
      collectPairs(value, (target as UnknownRecord)[key], dictionary);
    });
  }
}

function buildDictionary(locale: RuntimeLocale) {
  const dictionary: TextDictionary = {};
  collectPairs(uiCopy.en, uiCopy[locale], dictionary);
  collectPairs(authFlowCopy.en, authFlowCopy[locale], dictionary);
  collectPairs(resources.en, resources[locale], dictionary);
  return {
    ...dictionary,
    ...routeCopy[locale],
  };
}

const dictionaries: Record<RuntimeLocale, TextDictionary> = {
  ar: buildDictionary('ar'),
  zh: buildDictionary('zh'),
};

function translateRelativeTime(locale: RuntimeLocale, text: string) {
  const match = text.match(/^(\d+)\s+(min|minute|hour|day|week|month|year)s?\s+ago$/i);
  if (!match) return undefined;

  const value = Number(match[1]);
  const unit = match[2].toLowerCase();

  if (locale === 'zh') {
    const units: Record<string, string> = {
      min: '分钟',
      minute: '分钟',
      hour: '小时',
      day: '天',
      week: '周',
      month: '个月',
      year: '年',
    };
    return `${value} ${units[unit]}前`;
  }

  const units: Record<string, [string, string, string]> = {
    min: ['دقيقة', 'دقيقتين', 'دقائق'],
    minute: ['دقيقة', 'دقيقتين', 'دقائق'],
    hour: ['ساعة', 'ساعتين', 'ساعات'],
    day: ['يوم', 'يومين', 'أيام'],
    week: ['أسبوع', 'أسبوعين', 'أسابيع'],
    month: ['شهر', 'شهرين', 'أشهر'],
    year: ['سنة', 'سنتين', 'سنوات'],
  };
  const forms = units[unit];
  if (!forms) return undefined;
  if (value === 1) return `منذ ${forms[0]}`;
  if (value === 2) return `منذ ${forms[1]}`;
  return `منذ ${value} ${forms[2]}`;
}

function translatePattern(locale: RuntimeLocale, text: string) {
  const relativeTime = translateRelativeTime(locale, text);
  if (relativeTime) return relativeTime;

  const percentOff = text.match(/^(\d+)%\s+OFF$/i);
  if (percentOff) {
    return locale === 'zh' ? `${percentOff[1]}% 折扣` : `خصم ${percentOff[1]}%`;
  }

  const funded = text.match(/^(\d+)%\s+funded$/i);
  if (funded) {
    return locale === 'zh' ? `已融资 ${funded[1]}%` : `ممول بنسبة ${funded[1]}%`;
  }

  const itemCount = text.match(/^(\d+)\s+items?$/i);
  if (itemCount) {
    return locale === 'zh' ? `${itemCount[1]} 件商品` : `${itemCount[1]} عنصر`;
  }

  const days = text.match(/^(\d+)-(\d+)\s+days$/i);
  if (days) {
    return locale === 'zh' ? `${days[1]}-${days[2]} 天` : `${days[1]}-${days[2]} أيام`;
  }

  const businessDays = text.match(/^(\d+)-(\d+)\s+business days$/i);
  if (businessDays) {
    return locale === 'zh'
      ? `${businessDays[1]}-${businessDays[2]} 个工作日`
      : `${businessDays[1]}-${businessDays[2]} أيام عمل`;
  }

  const orderNumber = text.match(/^Order\s+#?(.+)$/i);
  if (orderNumber) {
    return locale === 'zh' ? `订单 ${orderNumber[1]}` : `الطلب ${orderNumber[1]}`;
  }

  return undefined;
}

export function translateRuntimeText(locale: UiLocale, value: string) {
  if (locale === 'en') return undefined;

  const text = normalizeText(value);
  if (!text || !/[A-Za-z]/.test(text)) return undefined;

  const exact = dictionaries[locale][text];
  if (exact) return exact;

  return translatePattern(locale, text);
}
